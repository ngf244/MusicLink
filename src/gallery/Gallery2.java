package gallery;

public class Gallery2 {
	
	private int glCode;
	private String glTitle;
	private String glDate;
	private String glContent;
	private int glStatus;
	private String userCode; 
	
	public int getGlCode() {
		return glCode;
	}
	public void setGlCode(int glCode) {
		this.glCode = glCode;
	}
	public String getGlTitle() {
		return glTitle;
	}
	public void setGlTitle(String glTitle) {
		this.glTitle = glTitle;
	}
	public String getGlDate() {
		return glDate;
	}
	public void setGlDate(String glDate) {
		this.glDate = glDate;
	}
	public String getGlContent() {
		return glContent;
	}
	public void setGlContent(String glContent) {
		this.glContent = glContent;
	}
	public int getGlStatus() {
		return glStatus;
	}
	public void setGlStatus(int glStatus) {
		this.glStatus = glStatus;
	}
	public String getUserCode() {
		return userCode;
	}
	public void setUserCode(String userCode) {
		this.userCode = userCode;
	}
	
}
